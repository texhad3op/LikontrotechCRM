package com.likontrotech.ejb.entities;

import java.io.Serializable;

public abstract class ClassifierType implements Serializable {
	int id;
	String name;

	public ClassifierType(int id, String name) {
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return this.name;
	}

}
